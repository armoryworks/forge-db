CREATE TABLE public.maintenance_predictions (
    id integer NOT NULL,
    work_center_id integer NOT NULL,
    prediction_type character varying(100) NOT NULL,
    confidence_percent numeric(5,2) NOT NULL,
    predicted_failure_date timestamp with time zone NOT NULL,
    remaining_useful_life_hours numeric(10,2),
    model_id character varying(100) NOT NULL,
    model_version character varying(50) NOT NULL,
    input_features_json jsonb NOT NULL,
    status integer NOT NULL,
    severity integer NOT NULL,
    predicted_at timestamp with time zone NOT NULL,
    acknowledged_at timestamp with time zone,
    acknowledged_by_user_id integer,
    preventive_maintenance_job_id integer,
    resolution_notes character varying(4000),
    was_accurate boolean NOT NULL
);

