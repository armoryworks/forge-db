    ADD CONSTRAINT fk_leads__outreach_campaigns_campaign_id FOREIGN KEY (campaign_id) REFERENCES public.outreach_campaigns(id) ON DELETE SET NULL;

ALTER TABLE ONLY public.leads
    ADD CONSTRAINT fk_leads_accounts_account_id FOREIGN KEY (account_id) REFERENCES public.accounts(id) ON DELETE SET NULL;

