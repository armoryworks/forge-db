CREATE TABLE public.prediction_feedbacks (
    id integer NOT NULL,
    prediction_id integer NOT NULL,
    actual_failure_occurred boolean NOT NULL,
    actual_failure_date timestamp with time zone,
    prediction_error_hours numeric(10,2),
    notes character varying(4000),
    recorded_by_user_id integer
);

ALTER TABLE public.prediction_feedbacks ALTER COLUMN id ADD GENERATED BY DEFAULT AS IDENTITY (
    SEQUENCE NAME public.prediction_feedbacks_id_seq
    START WITH 1
    INCREMENT BY 1
    NO MINVALUE
    NO MAXVALUE
    CACHE 1
);

ALTER TABLE ONLY public.prediction_feedbacks
    ADD CONSTRAINT pk_prediction_feedbacks PRIMARY KEY (id);

ALTER TABLE ONLY public.prediction_feedbacks
    ADD CONSTRAINT fk_prediction_feedbacks__asp_net_users_recorded_by_user_id FOREIGN KEY (recorded_by_user_id) REFERENCES public.asp_net_users(id) ON DELETE RESTRICT;

