    ADD CONSTRAINT fk_purchase_order_lines_mrp_planned_orders_mrp_planned_order_id FOREIGN KEY (mrp_planned_order_id) REFERENCES public.mrp_planned_orders(id) ON DELETE SET NULL;

ALTER TABLE ONLY public.purchase_order_lines
    ADD CONSTRAINT fk_purchase_order_lines_part_purchase_units_purchase_unit_id FOREIGN KEY (purchase_unit_id) REFERENCES public.part_purchase_units(id) ON DELETE RESTRICT;

ALTER TABLE ONLY public.purchase_order_lines
    ADD CONSTRAINT fk_purchase_order_lines_parts_part_id FOREIGN KEY (part_id) REFERENCES public.parts(id) ON DELETE RESTRICT;

ALTER TABLE ONLY public.purchase_order_lines
    ADD CONSTRAINT fk_purchase_order_lines_purchase_orders_purchase_order_id FOREIGN KEY (purchase_order_id) REFERENCES public.purchase_orders(id) ON DELETE CASCADE;
