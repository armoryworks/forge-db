    ADD CONSTRAINT fk_ppap_submissions__asp_net_users_psw_signed_by_user_id FOREIGN KEY (psw_signed_by_user_id) REFERENCES public.asp_net_users(id) ON DELETE RESTRICT;

ALTER TABLE ONLY public.ppap_submissions
    ADD CONSTRAINT fk_ppap_submissions_customers_customer_id FOREIGN KEY (customer_id) REFERENCES public.customers(id) ON DELETE RESTRICT;

